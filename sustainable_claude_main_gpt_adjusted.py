def esg_hex(score: float) -> str:
    """Gradient colour: red(0) → amber(50) → green(100)."""
    score = max(0.0, min(100.0, score))
    if score <= 50:
        t = score / 50.0
        r = 239; g = int(68  + t * (180 - 68));  b = int(68 + t * (8 - 68))
    else:
        t = (score - 50.0) / 50.0
        r = int(234 + t * (34  - 234))
        g = int(180 + t * (197 - 180))
        b = int(8   + t * (94  - 8))
    return f"#{r:02x}{g:02x}{b:02x}"


