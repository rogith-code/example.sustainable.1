def chip_html(value: float, suffix: str = "", decimals: int = 2) -> str:
    fmt = f"{value:+.{decimals}f}{suffix}"
    if value > 0.001:
        cls = "chip-pos"
    elif value < -0.001:
        cls = "chip-neg"
    else:
        cls = "chip-neu"
    return f'<span class="chip {cls}">{fmt}</span>'


def convert_to_100(score, agency: str) -> float:
    agency = agency.lower()
    if agency == "sustainalytics":
        v = float(score)
        return max(0.0, min(100.0, 100 - (v / 50) * 100)) if v < 50 else 0.0
    elif agency == "msci":
        msci_map = {
            "ccc": 0.0, "b": 16.7, "bb": 33.4, "bbb": 50.1,
            "a": 66.8, "aa": 83.5, "aaa": 100.0,
        }
        return msci_map.get(str(score).strip().lower(), 50.0)
    elif agency == "refinitiv":
        refinitiv_map = {0: 0, 1: 10, 2: 25, 3: 50, 4: 75, 5: 95}
        return float(refinitiv_map.get(int(float(score)), 50))
    else:  # s&p
        return max(0.0, min(100.0, float(score)))


def p_return(w1, r1, r2):   return w1 * r1 + (1 - w1) * r2
def p_sd(w1, sd1, sd2, rho):
    return np.sqrt(w1**2*sd1**2 + (1-w1)**2*sd2**2 + 2*rho*w1*(1-w1)*sd1*sd2)
def p_esg(w1, e1, e2):      return w1 * e1 + (1 - w1) * e2


