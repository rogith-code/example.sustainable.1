r_free = rf_pct / 100

weights = np.linspace(0, 1, 1000)

returns, risks, esg_scores_100, sharpe_ratios, utilities_esg, utilities_no_esg = [], [], [], [], [], []

for w in weights:
    pr = portfolio_return(w, r1, r2)
    ps = portfolio_sd(w, sd1, sd2, rho)
    pe_100 = portfolio_esg(w, esg1_100, esg2_100)
    pe_1   = portfolio_esg(w, esg1_1, esg2_1)
    returns.append(pr)
    risks.append(ps)
    esg_scores_100.append(pe_100)
    sh = (pr - r_free) / ps if ps > 0 else -np.inf
    sharpe_ratios.append(sh)
    utilities_esg.append(pr - (gamma/2)*ps**2 + lambda_esg*pe_1)
    utilities_no_esg.append(pr - (gamma/2)*ps**2)

def best(idx_arr):
    i = np.argmax(idx_arr)
    return (weights[i], 1-weights[i], returns[i], risks[i], esg_scores_100[i])

i_tan = np.argmax(sharpe_ratios)
i_esg = np.argmax(utilities_esg)
i_noesg = np.argmax(utilities_no_esg)

w1_tan,     w2_tan,     r_tan,    sd_tan,    esg_tan    = best(sharpe_ratios)
w1_esg,     w2_esg,     r_esg,    sd_esg,    esg_esg    = best(utilities_esg)
w1_noesg,   w2_noesg,   r_noesg,  sd_noesg,  esg_noesg  = best(utilities_no_esg)

