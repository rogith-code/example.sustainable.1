def convert_to_100(score, agency):
    agency = agency.lower()
    if agency == "sustainalytics":
        score = float(score)
        if score >= 50:
            return 0.0
        return max(0.0, min(100.0, 100 - (score / 50) * 100))
    elif agency == "msci":
        msci_map = {"ccc": 0, "b": 16.7, "bb": 33.4, "bbb": 50.1, "a": 66.8, "aa": 83.5, "aaa": 100.0}
        return msci_map[str(score).lower()]
    elif agency == "refinitiv":
        refinitiv_map = {0: 0.0, 1: 10.0, 2: 25.0, 3: 50.0, 4: 75.0, 5: 95.0}
        return refinitiv_map[int(float(score))]
    elif agency == "s&p":
        return max(0.0, min(100.0, float(score)))

def portfolio_return(w1, r1, r2):
    return w1 * r1 + (1 - w1) * r2

def portfolio_sd(w1, sd1, sd2, rho):
    return np.sqrt(w1**2 * sd1**2 + (1-w1)**2 * sd2**2 + 2*rho*w1*(1-w1)*sd1*sd2)

def portfolio_esg(w1, esg1, esg2):
    return w1 * esg1 + (1 - w1) * esg2

def esg_color(score):
    if score >= 70: return "#52e89b"
    if score >= 45: return "#f6c90e"
    return "#f87171"

def esg_label(score):
    if score >= 70: return "Strong"
    if score >= 45: return "Moderate"
    return "Weak"

